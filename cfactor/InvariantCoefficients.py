def feature_vector_cubic_miller_paper(h: int, k: int, l: int, cell: Cell) -> tuple[list[str], np.ndarray]:
    """
    Cubic paper-style:
      Cbar = [E1(h^4+k^4+l^4) + 2E2(h^2k^2+h^2l^2+k^2l^2)] * (d(hkl)^4 / a^4)
    """
    a = float(cell.a)
    d = d_spacing(cell, h, k, l)
    scale = (d**4) / (a**4)

    h2, k2, l2 = h*h, k*k, l*l
    h4, k4, l4 = h2*h2, k2*k2, l2*l2

    names = ["E1", "E2"]
    x = np.array([
        (h4 + k4 + l4) * scale,
        (2.0 * (h2*k2 + h2*l2 + k2*l2)) * scale,
    ], dtype=float)
    return names, x


def feature_vector_tetragonal_75_88_paper(h: int, k: int, l: int, cell: Cell) -> tuple[list[str], np.ndarray]:
    """
    Martinez-Garcia et al. tetragonal 4/m (75..88):
      [E1(h^4+k^4) + E2 l^4 + 2E3 h^2 k^2 + 2E4 l^2(h^2+k^2) + 4E5 h k (h^2-k^2)]
      / [h^2 + k^2 + (a/c)^2 l^2]^2
    """
    a = cell.a
    c = cell.c
    r = (a / c) ** 2

    h2, k2, l2 = h*h, k*k, l*l
    h4, k4, l4 = h2*h2, k2*k2, l2*l2

    denom = (h2 + k2 + r * l2) ** 2

    names = ["E1", "E2", "E3", "E4", "E5"]
    x = np.array([
        (h4 + k4) / denom,
        (l4) / denom,
        (2*h2*k2) / denom,
        (2*l2*(h2 + k2)) / denom,
        (4*h*k*(h2 - k2)) / denom,
    ], dtype=float)
    return names, x


def feature_vector_tetragonal_89_142_paper(h: int, k: int, l: int, cell: Cell) -> tuple[list[str], np.ndarray]:
    a = cell.a
    c = cell.c
    r = (a / c) ** 2

    h2, k2, l2 = h*h, k*k, l*l
    h4, k4, l4 = h2*h2, k2*k2, l2*l2

    denom = (h2 + k2 + r * l2) ** 2

    names = ["E1", "E3", "E4", "E5"]
    x = np.array([
        (h4 + k4) / denom,
        (l4) / denom,
        (2*h2*k2) / denom,
        (2*l2*(h2 + k2)) / denom,
    ], dtype=float)
    return names, x


def feature_vector_tetragonal_paper(h: int, k: int, l: int, cell: Cell) -> tuple[list[str], np.ndarray]:
    """
    Paper definition:
      [E1(h^4+k^4) + E3 l^4 + 2E4 h^2 k^2 + 2E5 l^2(h^2+k^2)] / d^4
    with tetragonal:
      1/d^2 = (h^2+k^2)/a^2 + l^2/c^2
    """
    a = cell.a
    c = cell.c

    h2, k2, l2 = h*h, k*k, l*l
    h4, k4, l4 = h2*h2, k2*k2, l2*l2

    inv_d2 = (h2 + k2) / (a*a) + (l2) / (c*c)
    inv_d4 = inv_d2 * inv_d2

    names = ["E1", "E3", "E4", "E5"]
    x = np.array([
        (h4 + k4) * inv_d4,
        (l4)      * inv_d4,
        (2*h2*k2) * inv_d4,
        (2*l2*(h2 + k2)) * inv_d4,
    ], dtype=float)
    return names, x


