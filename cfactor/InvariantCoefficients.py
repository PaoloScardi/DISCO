def run_fit(
    contrast_in_path: str | Path | None = None,
    input_table: str | Path | None = None,
    output_dir: str | Path = OUTPUT_DIR,
    out_prefix: str = OUT_PREFIX,
) -> tuple[
    dict[str, float], dict[str, float],   # E_screw_topas, metrics_screw_topas
    dict[str, float], dict[str, float],   # E_edge_topas,  metrics_edge_topas
    dict[str, float], dict[str, float],   # E_screw_paper, metrics_screw_paper
    dict[str, float], dict[str, float],   # E_edge_paper,  metrics_edge_paper
    Path, Path,                           # csv_screw_topas, csv_edge_topas
    Path, Path                            # csv_screw_paper, csv_edge_paper
]:
    """
    Runs invariant fits in two modes:

      1) TOPAS: direction-cosines feature vector (your original macro logic)
      2) PAPER: (kept only for tetragonal + cubic). For all other SG, falls back to TOPAS.

    NOTE:
      Monoclinic PAPER normalization removed.
    """
    # Load contrast_factors.in
    if contrast_in_path is None:
        contrast_in_path = _resolve_contrast_input_path()
    else:
        contrast_in_path = Path(contrast_in_path).resolve()

    with Path(contrast_in_path).open("r", encoding="utf-8") as f:
        contrast_in = json.load(f)

    sg_file = int(
        contrast_in.get("space_group")
        or _default_sg_for_lattice(str(contrast_in["cell"]["lattice"]))
    )
    cell_file = _load_cell_from_contrast_in(contrast_in)

    sg = FORCE_SG if FORCE_SG is not None else sg_file
    if FORCE_CELL is not None:
        a, b, c, al, be, ga = FORCE_CELL
        cell = Cell(a=a, b=b, c=c, alpha=al, beta=be, gamma=ga)
    else:
        cell = cell_file

    # Resolve input table path
    if input_table is None:
        if FORCE_INPUT_TABLE:
            in_table = Path(FORCE_INPUT_TABLE).resolve()
        else:
            in_table = _output_table_path_from_contrast_in(Path(contrast_in_path))
    else:
        in_table = Path(input_table).resolve()

    if not in_table.exists():
        raise FileNotFoundError(
            f"Contrast-factor table not found: {in_table}\n"
            "Run main.py first (to generate the table) or set FORCE_INPUT_TABLE."
        )

    df = parse_project_table(str(in_table))

    # Output paths
    os.makedirs(str(output_dir), exist_ok=True)
    base = (Path(output_dir) / out_prefix).resolve()

    # TOPAS fits
    E_screw_topas, tab_screw_topas, met_screw_topas = fit_case_with_mode(
        df, sg, cell, obs_col="C_phi0", mode="topas"
    )
    E_edge_topas, tab_edge_topas, met_edge_topas = fit_case_with_mode(
        df, sg, cell, obs_col="C_phi90", mode="topas"
    )

    # PAPER fits (tetragonal/cubic only; others fallback to TOPAS)
    E_screw_paper, tab_screw_paper, met_screw_paper = fit_case_with_mode(
        df, sg, cell, obs_col="C_phi0", mode="paper"
    )
    E_edge_paper, tab_edge_paper, met_edge_paper = fit_case_with_mode(
        df, sg, cell, obs_col="C_phi90", mode="paper"
    )

    # Write CSVs
    csv_screw_topas = Path(f"{base}_comparison_screw_TOPAS.csv").resolve()
    csv_edge_topas  = Path(f"{base}_comparison_edge_TOPAS.csv").resolve()
    csv_screw_paper = Path(f"{base}_comparison_screw_PAPER.csv").resolve()
    csv_edge_paper  = Path(f"{base}_comparison_edge_PAPER.csv").resolve()

    tab_screw_topas.to_csv(csv_screw_topas, index=False)
    tab_edge_topas.to_csv(csv_edge_topas, index=False)
    tab_screw_paper.to_csv(csv_screw_paper, index=False)
    tab_edge_paper.to_csv(csv_edge_paper, index=False)

    return (
        E_screw_topas, met_screw_topas,
        E_edge_topas,  met_edge_topas,
        E_screw_paper, met_screw_paper,
        E_edge_paper,  met_edge_paper,
        csv_screw_topas, csv_edge_topas,
        csv_screw_paper, csv_edge_paper
    )


def run_fit_multi_phi(
    contrast_in_path: str | Path | None = None,
    input_table: str | Path | None = None,
    output_dir: str | Path = OUTPUT_DIR,
    out_prefix: str = OUT_PREFIX,
) -> tuple[dict[float, dict[str, float]], dict[float, dict[str, float]], dict[float, Path]]:
    """
    Runs invariant fit for EACH phi present in the table.
    Returns:
      (E_by_phi, metrics_by_phi, csv_by_phi)

    NOTE:
      This function uses fit_case(), which:
        - keeps tetragonal/cubic paper feature vectors
        - falls back to TOPAS direction-cosines otherwise
        - NO monoclinic paper normalization.
    """
    if contrast_in_path is None:
        contrast_in_path = _resolve_contrast_input_path()
    else:
        contrast_in_path = Path(contrast_in_path).resolve()

    with Path(contrast_in_path).open("r", encoding="utf-8") as f:
        contrast_in = json.load(f)

    sg_file = int(
        contrast_in.get("space_group")
        or _default_sg_for_lattice(str(contrast_in["cell"]["lattice"]))
    )
    cell_file = _load_cell_from_contrast_in(contrast_in)

    sg = FORCE_SG if FORCE_SG is not None else sg_file
    cell = Cell(*FORCE_CELL) if FORCE_CELL is not None else cell_file  # type: ignore[arg-type]

    if input_table is None:
        if FORCE_INPUT_TABLE:
            in_table = Path(FORCE_INPUT_TABLE).resolve()
        else:
            in_table = _output_table_path_from_contrast_in(Path(contrast_in_path))
    else:
        in_table = Path(input_table).resolve()

    if not in_table.exists():
        raise FileNotFoundError(f"Contrast-factor table not found: {in_table}")

    df, phi_values = parse_project_table_multi_phi(str(in_table))

    os.makedirs(str(output_dir), exist_ok=True)
    base = Path(output_dir) / out_prefix

    E_by_phi: dict[float, dict[str, float]] = {}
    metrics_by_phi: dict[float, dict[str, float]] = {}
    csv_by_phi: dict[float, Path] = {}

    for phi in phi_values:
        col = f"C_phi{phi:g}"
        E_fit, tab, met = fit_case(df, sg, cell, obs_col=col)

        csv_path = Path(f"{base}_comparison_phi{phi:g}.csv").resolve()
        tab.to_csv(csv_path, index=False)

        E_by_phi[phi] = E_fit
        metrics_by_phi[phi] = met
        csv_by_phi[phi] = csv_path

    return E_by_phi, metrics_by_phi, csv_by_phi


