def _default_sg_for_lattice(lattice: str) -> int:
    lat = lattice.lower()
    if lat == "triclinic":
        return 1
    if lat == "monoclinic":
        return 5
    if lat == "orthorhombic":
        return 62
    if lat == "tetragonal":
        return 89
    if lat in ("trigonal", "rhombohedral"):
        return 149
    if lat == "hexagonal":
        return 168
    if lat == "cubic":
        return 225
    raise ValueError(f"Unsupported lattice '{lattice}' for SG default mapping.")


def _output_table_path_from_contrast_in(contrast_in_path: Path) -> Path:
    with contrast_in_path.open("r", encoding="utf-8") as f:
        data = json.load(f)

    material_name = str(data.get("material_name", "Material"))

    cellj = data.get("cell", {})
    lattice = str(cellj.get("lattice", "")).lower()

    slip0 = data["slip_systems"][0]
    plane_hkl = slip0["plane_hkl"]
    burgers_uvw = slip0["burgers_uvw"]

    plane_str = "_".join(str(int(x)) for x in plane_hkl)
    burg_str = "_".join(str(int(x)) for x in burgers_uvw)

    out_name = f"{material_name}_{lattice}_plane[{plane_str}]_burgers[{burg_str}].txt"
    return (contrast_in_path.parent / out_name).resolve()


def _load_cell_from_contrast_in(data: dict) -> Cell:
    # Use table-driven symmetry constraints (so hexagonal a,c implies gamma=120, b=a, ...)
    return _cell_from_json_table_driven(data)


def parse_project_table(path: str) -> pd.DataFrame:
    rows: list[tuple[int, int, int, float, float, float, float]] = []
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            raw = line.strip()
            if not raw or raw.startswith("#"):
                continue
            parts = raw.split()
            if len(parts) < 7:
                continue
            h, k, l = map(int, parts[0:3])
            delta0 = float(parts[3])
            c0 = float(parts[4])
            delta90 = float(parts[5])
            c90 = float(parts[6])
            rows.append((h, k, l, delta0, c0, delta90, c90))

    if not rows:
        raise ValueError(f"No data rows found in '{path}'.")
    return pd.DataFrame(
        rows,
        columns=["h", "k", "l", "delta_phi0", "C_phi0", "delta_phi90", "C_phi90"],
    )


def parse_project_table_multi_phi(path: str) -> tuple[pd.DataFrame, list[float]]:
    """
    Supports the extended table written by main.py:
      h k l  (delta/H, C)_phi1  (delta/H, C)_phi2  ...

    Returns:
      (df, phi_values)
    where df contains columns:
      h,k,l plus: delta_phi{phi}, C_phi{phi} for each phi.
    """
    header_phi: list[float] = []
    rows: list[list[float]] = []
    hkls: list[tuple[int, int, int]] = []

    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            raw = line.strip()
            if not raw:
                continue

            if raw.startswith("#"):
                parts = raw.replace("#", "").split()
                header_phi = []
                for p in parts:
                    if p.startswith("C_eq_avg_phi"):
                        header_phi.append(float(p[len("C_eq_avg_phi"):]))
                continue

            parts = raw.split()
            if len(parts) < 3:
                continue

            h, k, l = map(int, parts[0:3])
            hkls.append((h, k, l))

            nums = list(map(float, parts[3:]))
            rows.append(nums)

    if not hkls:
        raise ValueError(f"No data rows found in '{path}'.")
    if not header_phi:
        raise ValueError(f"Could not parse phi columns from header in '{path}'.")

    expected = 2 * len(header_phi)
    for r in rows:
        if len(r) < expected:
            raise ValueError(f"Row has {len(r)} numeric fields, expected {expected}.")

    df = pd.DataFrame(hkls, columns=["h", "k", "l"])
    for i, phi in enumerate(header_phi):
        df[f"delta_phi{phi:g}"] = [r[2 * i + 0] for r in rows]
        df[f"C_phi{phi:g}"] = [r[2 * i + 1] for r in rows]

    return df, header_phi


