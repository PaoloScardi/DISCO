@dataclass(frozen=True)
class Cell:
    a: float
    b: float
    c: float
    alpha: float = 90.0
    beta: float = 90.0
    gamma: float = 90.0


def _infer_lattice_from_space_group(space_group: int) -> str:
    sg = int(space_group)
    if sg < 1 or sg > 230:
        raise ValueError(f"space_group must be in 1..230, got {space_group}")
    if sg <= 2:
        return "triclinic"
    if sg <= 15:
        return "monoclinic"
    if sg <= 74:
        return "orthorhombic"
    if sg <= 142:
        return "tetragonal"
    if sg <= 167:
        return "trigonal"
    if sg <= 194:
        return "hexagonal"
    return "cubic"


def _cell_from_json_table_driven(data: dict) -> Cell:
    """
    Build a complete Cell using a table-driven convention (matches project rules):

      cubic:        a
      tetragonal:   a, c        => b=a; alpha=beta=gamma=90
      hexagonal:    a, c        => b=a; alpha=beta=90; gamma=120
      trigonal:     a, c (hex setting) => b=a; alpha=beta=90; gamma=120
      orthorhombic: a, b, c     => alpha=beta=gamma=90
      monoclinic:   a, b, c, beta => alpha=gamma=90
      triclinic:    a, b, c, alpha, beta, gamma
    """
    cell_json = data.get("cell", {})
    if not isinstance(cell_json, dict):
        raise ValueError("cell must be an object")

    sg = data.get("space_group", data.get("space_group_number", None))
    lattice_in = cell_json.get("lattice", None)

    lattice = str(lattice_in).lower() if lattice_in is not None else None
    if lattice is None:
        if sg is None:
            raise ValueError("Input must provide either cell.lattice or space_group")
        lattice = _infer_lattice_from_space_group(int(sg))

    def req(name: str) -> float:
        if name not in cell_json:
            raise ValueError(f"cell.{name} is required for lattice='{lattice}'")
        return float(cell_json[name])

    if lattice == "cubic":
        a = req("a")
        return Cell(a=a, b=a, c=a, alpha=90.0, beta=90.0, gamma=90.0)

    if lattice == "tetragonal":
        a = req("a")
        c = req("c")
        return Cell(a=a, b=a, c=c, alpha=90.0, beta=90.0, gamma=90.0)

    if lattice == "hexagonal":
        a = req("a")
        c = req("c")
        return Cell(a=a, b=a, c=c, alpha=90.0, beta=90.0, gamma=120.0)

    if lattice == "trigonal":
        # Default to hexagonal axes setting (a,c) as per your table.
        a = req("a")
        c = req("c")
        return Cell(a=a, b=a, c=c, alpha=90.0, beta=90.0, gamma=120.0)

    if lattice == "orthorhombic":
        a = req("a")
        b = req("b")
        c = req("c")
        return Cell(a=a, b=b, c=c, alpha=90.0, beta=90.0, gamma=90.0)

    if lattice == "monoclinic":
        a = req("a")
        b = req("b")
        c = req("c")
        beta = req("beta")
        return Cell(a=a, b=b, c=c, alpha=90.0, beta=beta, gamma=90.0)

    if lattice == "triclinic":
        a = req("a")
        b = req("b")
        c = req("c")
        alpha = req("alpha")
        beta = req("beta")
        gamma = req("gamma")
        return Cell(a=a, b=b, c=c, alpha=alpha, beta=beta, gamma=gamma)

    raise ValueError(f"Unsupported lattice='{lattice}'")


def _deg2rad(x: float) -> float:
    return np.deg2rad(x)


def _is_close(x: float, y: float, tol: float = 1e-6) -> bool:
    return abs(x - y) <= tol


def reciprocal_params(cell: Cell):
    a, b, c = cell.a, cell.b, cell.c
    al, be, ga = map(_deg2rad, (cell.alpha, cell.beta, cell.gamma))

    ca, cb, cg = np.cos(al), np.cos(be), np.cos(ga)
    sa, sb, sg = np.sin(al), np.sin(be), np.sin(ga)

    V = a * b * c * np.sqrt(1.0 + 2.0 * ca * cb * cg - ca**2 - cb**2 - cg**2)

    astar = (b * c * sa) / V
    bstar = (a * c * sb) / V
    cstar = (a * b * sg) / V

    cos_al_star = (cb * cg - ca) / (sb * sg)
    cos_be_star = (ca * cg - cb) / (sa * sg)
    cos_ga_star = (ca * cb - cg) / (sa * sb)

    cos_al_star = np.clip(cos_al_star, -1.0, 1.0)
    cos_be_star = np.clip(cos_be_star, -1.0, 1.0)
    cos_ga_star = np.clip(cos_ga_star, -1.0, 1.0)

    al_star = np.arccos(cos_al_star)
    be_star = np.arccos(cos_be_star)
    ga_star = np.arccos(cos_ga_star)

    return astar, bstar, cstar, al_star, be_star, ga_star


def _direct_basis_cart(cell: Cell) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    """
    Build direct lattice basis vectors a,b,c in Cartesian coordinates.
    Convention:
      a along x
      b in xy plane
      c with general orientation
    """
    a, b, c = cell.a, cell.b, cell.c
    al = np.deg2rad(cell.alpha)
    be = np.deg2rad(cell.beta)
    ga = np.deg2rad(cell.gamma)

    ax = np.array([a, 0.0, 0.0], dtype=float)
    bx = np.array([b * np.cos(ga), b * np.sin(ga), 0.0], dtype=float)

    cx_x = c * np.cos(be)
    cx_y = c * (np.cos(al) - np.cos(be) * np.cos(ga)) / np.sin(ga)
    cx_z_sq = c**2 - cx_x**2 - cx_y**2
    cx_z = np.sqrt(max(cx_z_sq, 0.0))
    cx = np.array([cx_x, cx_y, cx_z], dtype=float)

    return ax, bx, cx


def _reciprocal_basis_cart(cell: Cell) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    """
    Reciprocal basis vectors a*, b*, c* in Cartesian coordinates (2π omitted).
    """
    a, b, c = _direct_basis_cart(cell)
    V = np.dot(a, np.cross(b, c))
    if abs(V) < 1e-18:
        raise ValueError("Cell volume is zero or numerically unstable.")

    astar = np.cross(b, c) / V
    bstar = np.cross(c, a) / V
    cstar = np.cross(a, b) / V
    return astar, bstar, cstar


def d_spacing(cell: Cell, h: int, k: int, l: int) -> float:
    """
    d(hkl) using the same geometry already used in HKL_direction_cosines():
      g = h a* + k b* + l c*   (2π omitted)
      |g| = 1/d
    """
    astar, bstar, cstar = _reciprocal_basis_cart(cell)
    g = h * astar + k * bstar + l * cstar
    gnorm = float(np.linalg.norm(g))
    if gnorm <= 0.0:
        raise ValueError(f"Non-positive |g| for hkl=({h},{k},{l}).")
    return 1.0 / gnorm


def HKL_direction_cosines(h: int, k: int, l: int, cell: Cell) -> Tuple[float, float, float]:
    """
    True direction cosines of g = h a* + k b* + l c* with respect to the
    reciprocal basis directions (unit vectors along a*, b*, c*), computed in Cartesian space.
    """
    astar, bstar, cstar = _reciprocal_basis_cart(cell)

    g = h * astar + k * bstar + l * cstar
    gnorm = float(np.linalg.norm(g))
    if gnorm <= 0.0:
        raise ValueError(f"Non-positive |g| for hkl=({h},{k},{l}).")

    ea = astar / np.linalg.norm(astar)
    eb = bstar / np.linalg.norm(bstar)
    ec = cstar / np.linalg.norm(cstar)

    H = float(np.dot(g, ea) / gnorm)
    K = float(np.dot(g, eb) / gnorm)
    L = float(np.dot(g, ec) / gnorm)
    return H, K, L


